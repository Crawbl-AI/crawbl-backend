// Agent represents an AI agent within a workspace swarm.
// Each workspace can have multiple agents with different roles.
type Agent struct {
	// ID is the unique identifier for the agent.
	ID string `json:"id"`

	// WorkspaceID is the workspace this agent belongs to.
	WorkspaceID string `json:"workspace_id"`

	// Name is the display name of the agent.
	Name string `json:"name"`

	// Role describes the agent's role in the swarm hierarchy (e.g., "sub-agent", "manager").
	Role string `json:"role"`

	// Slug is the agent runtime routing identifier for this agent.
	// Matches the [agents.<slug>] key in config.toml and is sent as agent_id in the webhook.
	Slug string `json:"slug"`

	// AvatarURL is the URL to the agent's avatar image.
	AvatarURL string `json:"avatar"`

	// Status indicates the current operational status of the agent.
	// Emitted with omitempty so an unset status serialises as no field
	// at all (not "") — mobile's AgentStatus enum parse would otherwise
	// reject an empty string on incoming socket events.
	Status AgentStatus `json:"status,omitempty"`

	// SystemPrompt is the LLM system message for this agent's personality.
	SystemPrompt string `json:"system_prompt,omitempty"`

	// Description is a short human-readable summary of the agent's purpose.
	Description string `json:"description"`

	// CreatedAt is the timestamp when the agent was created.
	CreatedAt time.Time `json:"created_at"`

	// UpdatedAt is the timestamp when the agent was last modified.
	UpdatedAt time.Time `json:"updated_at"`
}

