// GetDefaultAgents returns the list of default agent blueprints for new workspaces.
// Data is loaded from the embedded JSON seed files at compile time.
func GetDefaultAgents() []DefaultAgentBlueprint {
	entries := seed.DefaultAgents()
	out := make([]DefaultAgentBlueprint, len(entries))
	for i, e := range entries {
		out[i] = DefaultAgentBlueprint{
			Name:         e.Name,
			Slug:         e.Slug,
			Role:         e.Role,
			SystemPrompt: e.SystemPrompt,
			Description:  e.Description,
			AllowedTools: e.AllowedTools,
		}
	}
	return out
}

